package com.java.learning.completeJava.trickyPrograms;

public class Dollar {

	/*
	 * It looks like $ will cause an error, but it won’t. In java, identifier rule
	 * says, identifier can start with any alphabet or underscore (“_”) or dollar
	 * (“$”). So answer is Nothing.
	 */

	public static void main(String[] args) {
		int $_ = 5;

	}

}
